const users = [];

const applications = [];

function getOrCreateUser(userId, profile = {}) {
  if (!userId) return null;

  const existing = users.find((u) => u._id === userId);
  if (existing) {
    if (profile.name) existing.name = profile.name;
    if (profile.email) existing.email = profile.email;
    if (profile.avatar) existing.avatar = profile.avatar;
    if (profile.google_id) existing.google_id = profile.google_id;
    return existing;
  }

  const isGoogleUser = userId.startsWith('google_');
  const newUser = {
    _id: userId,
    name: profile.name || (isGoogleUser ? 'Google User' : 'User'),
    email: profile.email || '',
    avatar: profile.avatar || '',
    google_id: profile.google_id || (isGoogleUser ? userId.replace('google_', '') : ''),
    skills: [],
    resume_url: '',
    resume_text: '',
  };
  users.push(newUser);
  return newUser;
}

